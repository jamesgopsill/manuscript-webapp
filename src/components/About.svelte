<h1>Welcome to ManuScript</h1>

<p>
	ManuScript is a new academic writing platform that brings research article
	writing into the 21st century by building them specifically for the web. The
	platform works in a similar format to LaTeX but rather than compile to a pdf,
	ManuScript creates a HTML page.
</p>

<p>
	This means that your research paper is now designed for the web and can be
	read on any device anywhere in the world. The paper will automatically fit to
	the screens size so you won&#39;t have to zoom and pan around a pdf on your
	mobile device anymore!
</p>

<p>It also enables richer content to be embedded within the paper including:</p>

<ul class="fa-ul">
	<li><i class="fa-li fa fa-video-camera" /> HD &amp; 4K Video</li>
	<li><i class="fa-li fa fa-picture-o" /> High-Resolution Imagery</li>
	<li><i class="fa-li fa fa-volume-up" /> High-Quality &amp; Lossless Audio</li>
	<li><i class="fa-li fa fa-code" /> Syntax Highlighted Code</li>
	<li><i class="fa-li fa fa-database" /> Open Access Datasets</li>
</ul>

<p>
	This further enhances the reporting and re-usability of your research and also
	enhances the search-ability of your papers so that other researchers will be
	able to find your research quicker and easier <i class="fa fa-smile-o" />.
</p>

<p>
	The web editor and underlying compiler is opensource and we want to work with
	publishers and research community so they can incorporate ManuScript into
	their publication workflows.
</p>

